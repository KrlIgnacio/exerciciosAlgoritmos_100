public class Exercicio_05 {

	public static void main(String[] args) {
		// For regressivo de 100 a 0 de 10 em 10
		for (int i = 100; i>=0; i-=10) {
			System.out.print(" "+i);
		}

		System.out.println(" Acabou!");
	}

}
