public class Exercicio_01 {

	public static void main(String[] args) {
		// do-while de zero a 30 de 3 em 3
		int i = -3;
		do {
			i = i + 3;
			System.out.print(" "+i);
		}while(i < 30);
		System.out.print(" Acabou!");

	}

}
