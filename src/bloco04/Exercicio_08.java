public class Exercicio_08 {

	public static void main(String[] args) {
		//Faça uma contagem crescente do 6 ao 11	
		int num = 5;
		//enquanto num for menor que 11 some +1
		while(num < 11) {
			num ++;
			System.out.print(" "+ num);
		}
	//Exercicio com a estrutura WHILE, porém funciona perfeitamente com a estrutura FOR
	}

}
